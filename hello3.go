package main

import "fmt"

func main() {
    //declarando la variable inicial
    var a string = "inicial"
    fmt.Println(a)
    
//    declarando más de una variable.
    var b,c,h int = 4,5,0
    fmt.Println(b,c,h)
    
    //Declarando variables con resultado booleano
    var d = true
    fmt.Println(d)
    
    //Declarando variable e de tipo entero
    var e int
    fmt.Println(e)
    
    //declaración de una cadena con otro tipo de sintáxis.
    f:= "short"
    fmt.Println(f)
    
}